# dcbz on Xbox 360 Xenon zeroes a full 128-byte cache line (there is no 32-byte
# form on this target). EA is aligned DOWN to the 128-byte boundary, then 128
# bytes are zeroed. A buggy 32-byte model (align ~31, memset 32) would align the
# unaligned EA below to 0x10001040 and zero only [0x10001040, 0x10001060),
# leaving the rest of the pre-filled line non-zero -> this test would then fail.

test_dcbz_1:
  # Pre-fill the whole 128-byte line [0x10001000, 0x10001080) with a non-zero pattern.
  #_ MEMORY_IN 10001000 11111111 22222222 33333333 44444444 55555555 66666666 77777777 88888888 99999999 AAAAAAAA BBBBBBBB CCCCCCCC DDDDDDDD EEEEEEEE FFFFFFFF 10101010
  # Unaligned EA inside the line; must align down to 0x10001000.
  #_ REGISTER_IN r4 0x10001044
  dcbz 0, r4
  blr
  #_ REGISTER_OUT r4 0x10001044
  #_ MEMORY_OUT 10001000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000

test_dcbz_1_ra:
  # Same, but split the EA across RA+RB to exercise the RA!=0 path.
  #_ MEMORY_IN 10001000 11111111 22222222 33333333 44444444 55555555 66666666 77777777 88888888 99999999 AAAAAAAA BBBBBBBB CCCCCCCC DDDDDDDD EEEEEEEE FFFFFFFF 10101010
  #_ REGISTER_IN r3 0x10001000
  #_ REGISTER_IN r4 0x00000044
  dcbz r3, r4
  blr
  #_ REGISTER_OUT r3 0x10001000
  #_ REGISTER_OUT r4 0x00000044
  #_ MEMORY_OUT 10001000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000 00000000
